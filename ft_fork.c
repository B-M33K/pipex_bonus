#include"pipex.h"

void	ft_fork(t_cmd *cmd)
{
	pid_t	pid;

	pid = fork();
	if (pid == -1)
	{
		perror("fork failed");
		exit(1);
	}
	if (!pid)
		ft_execve(cmd);
}
